    public void refreshPopulate() {//??
        DefaultTableModel dtm = (DefaultTableModel) tblProduct.getModel();
        dtm.setRowCount(0);
        for (Product p : enterprise.getProductCatalog().getProductCatalog()) {//because declare another arraylist in productCatalog
            Object row[] = new Object[3];
            row[0] = p;
            row[1] = p.getModelNumber();
            row[2] = p.getPrice();
            dtm.addRow(row);
        }
    }

