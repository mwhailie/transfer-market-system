    public boolean isNumber(String str){
        boolean isNum =false;
        for (int i = str.length();--i>=0;){  
            if (!Character.isDigit(str.charAt(i))){
            isNum = false;
            break;
            }
        isNum = true;
        }
        if (isNum) {
            return true;
        } else {
            JOptionPane.showMessageDialog(null, "Please fill a number less than 5");
            return false;
        }
    }
