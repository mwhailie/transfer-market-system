    private void btnSendContractActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSendContractActionPerformed
        // TODO add your handling code here:
        int row = contractRequestJTable.getSelectedRow();
        if (txtSignature.getText().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Please sign your name!!", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        if (row < 0) {
            JOptionPane.showMessageDialog(null, "Please select a row!!", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        ContractWorkRequest request = (ContractWorkRequest) contractRequestJTable.getValueAt(row, 0);

        request.setStatus("Completed");
        enterprise.getEmployeeDirectory().removeEmployee(account.getEmployee());
        enterprise.getUserAccountDirectory().removeUserAccount(account);

        PlayerRole playerRole = (PlayerRole) account.getRole();
        Enterprise buyer = request.getBuyer();
        int cost = request.getSignOnFee();
        int newClubCash = enterprise.getClubCash() + cost;
        int newClubCash2 = buyer.getClubCash() - cost;

        if (JOptionPane.showConfirmDialog(null, "Are you sure?", "WARNING",
                JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION) {
            // yes option

            Contract newContract = playerRole.addContract();
            newContract.setBonus(request.getBonus());
            newContract.setBuyer(buyer);
            newContract.setContractEndDate(request.getContractEndDate());
            newContract.setContractStartDate(request.getContractStartDate());
            newContract.setSeller(request.getSeller());
            newContract.setSignOnFee(cost);
            newContract.setWage(request.getWage());

            enterprise.getSellingContractList().add(newContract);
            buyer.getBuyingContractList().add(newContract);
            
            enterprise.setClubCash(newClubCash);
            buyer.setClubCash(newClubCash2);
            for (Organization o : buyer.getOrganizationDirectory().getOrganizationList()) {
                if (o instanceof PlayerOrganization) {
                    o.getUserAccountDirectory().transferUserAccount(account);
                    o.getEmployeeDirectory().transferEmployee(account.getEmployee());
                }
            }

            JOptionPane.showMessageDialog(null, "Transfer Successful!!");
            request.setStatus("Success!");
        }


    }//GEN-LAST:event_btnSendContractActionPerformed

