    public boolean validRequestNumber(String str){
        boolean isNum =false;
        for (int i = str.length();--i>=0;){  
            if (!Character.isDigit(str.charAt(i))){
            isNum = false;
            break;
            }
        isNum = true;
        }
        if (isNum && Integer.parseInt(str)<5) {
            return true;
        } else {
            JOptionPane.showMessageDialog(null, "Please fill a number less than 5");
            return false;
        }
    }

