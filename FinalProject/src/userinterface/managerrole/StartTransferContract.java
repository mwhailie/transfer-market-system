    public void populatePlayerTable() {
        DefaultTableModel model = (DefaultTableModel) playerJTable.getModel();

        model.setRowCount(0);

        for (WorkRequest request : managerOrganization.getWorkQueue().getWorkRequestList()) {
            if (request instanceof BossToManagerWorkRequest) {
                Object[] row = new Object[4];
                row[0] = (BossToManagerWorkRequest) request;
                row[1] = ((BossToManagerWorkRequest) request).getFloorPrice();
                row[2] = ((BossToManagerWorkRequest) request).getCeilingPrice();
                row[3] = ((BossToManagerWorkRequest) request).getTargetPrice();
                model.addRow(row);
            }
        }

    }

    private void populateEnterpriseComboBox() {
        cmbEnterprise.removeAllItems();

        for (Network network : system.getNetworkList()) {
            for (Enterprise enterprise : network.getEnterpriseDirectory().getEnterpriseList()) {
                if (!enterprise.equals(this.enterprise)) {
                    cmbEnterprise.addItem(enterprise);
                }
            }
        }
    }

