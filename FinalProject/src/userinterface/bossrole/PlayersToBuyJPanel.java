    private void setPriceBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_setPriceBtnActionPerformed
        // TODO add your handling code here:
        int selectedRow = playersToBuyTbl.getSelectedRow();

        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Please select a row", "Warning", JOptionPane.WARNING_MESSAGE);
            
        } else {
            ReportWorkRequest reportWorkRequest = (ReportWorkRequest) playersToBuyTbl.getValueAt(selectedRow, 0);
            reportWorkRequest.setStatus("Completed");
            UserAccount player = reportWorkRequest.getPlayer();
            
        SetPriceJPanel setPriceJPane = new SetPriceJPanel(userProcessContainer, account, bossOrganization, enterprise, player);
        userProcessContainer.add("setPriceJPane", setPriceJPane);
        CardLayout layout = (CardLayout) userProcessContainer.getLayout();
        layout.next(userProcessContainer);
        }
    }//GEN-LAST:event_setPriceBtnActionPerformed


