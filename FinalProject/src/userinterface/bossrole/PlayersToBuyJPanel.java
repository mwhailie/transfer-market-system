    public void populatePlayerTable() {
        DefaultTableModel model = (DefaultTableModel) playersToBuyTbl.getModel();

        model.setRowCount(0);
        
        for (WorkRequest request : bossOrganization.getWorkQueue().getWorkRequestList()) {
            if (request instanceof ReportWorkRequest) {
                UserAccount player = ((ReportWorkRequest)request).getPlayer();
                PlayerRole playerRole = (PlayerRole)player.getRole();
                Object[] row = new Object[4];
                row[0] = (ReportWorkRequest) request;
                row[1] = playerRole.getCurrentClubName();
                row[2] = playerRole.getPosition();
                row[3] = playerRole.getCommercialValue();
                model.addRow(row);
            }
        }

    }
    
    public void populateSetPriceHistoryTable() {
        DefaultTableModel model = (DefaultTableModel) setPriceHistoryTbl.getModel();

        model.setRowCount(0);
        
        for (WorkRequest request : account.getWorkQueue().getWorkRequestList()) {
            if (request instanceof BossToManagerWorkRequest) {
                BossToManagerWorkRequest bossToManagerWorkRequest= (BossToManagerWorkRequest)request;
                UserAccount player = bossToManagerWorkRequest.getPlayer();
               
                Object[] row = new Object[4];
                row[0] = player.getEmployee();
                row[1] = bossToManagerWorkRequest.getCeilingPrice();
                row[2] = bossToManagerWorkRequest.getFloorPrice();
                row[3] = bossToManagerWorkRequest.getTargetPrice();
                model.addRow(row);
            }
        }

    }

