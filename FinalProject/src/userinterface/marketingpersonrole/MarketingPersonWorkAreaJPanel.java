    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblProduct.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "please select a row from table first", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        } else {
            Product p = (Product) tblProduct.getValueAt(selectedRow, 0);
            enterprise.getProductCatalog().removeProduct(p);
            refreshPopulate();

        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnViewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnViewActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblProduct.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "please select a row from table first", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {
            Product product = (Product) tblProduct.getValueAt(selectedRow, 0);

                txtName.setText(product.getProdName());
                txtID.setText(String.valueOf(product.getModelNumber()));
                txtPrice.setText(String.valueOf(product.getPrice()));
        }
    }//GEN-LAST:event_btnViewActionPerformed


