    public void populatePlayerTable() {
        DefaultTableModel model = (DefaultTableModel) playerJTable.getModel();

        model.setRowCount(0);

        for (WorkRequest request : managerOrganization.getWorkQueue().getWorkRequestList()) {
            if (request instanceof ContractWorkRequest) {
                if (!((ContractWorkRequest) request).getBuyer().equals(enterprise)) {
                    Object[] row = new Object[3];
                    row[0] = (ContractWorkRequest) request;
                    row[1] = ((ContractWorkRequest) request).getSeller();
                    row[2] = ((ContractWorkRequest) request).getSignOnFee();
                    model.addRow(row);
                }
            }
        }

    }

