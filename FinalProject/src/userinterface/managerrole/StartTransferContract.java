    private void btnSendContractActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSendContractActionPerformed
        // TODO add your handling code here:
        int row = playerJTable.getSelectedRow();
        if (row < 0) {
            JOptionPane.showMessageDialog(null, "Please select a row!!", "Warning", JOptionPane.WARNING_MESSAGE);
        }

        BossToManagerWorkRequest request = (BossToManagerWorkRequest) playerJTable.getValueAt(row, 0);
        UserAccount player = request.getPlayer();
        int wage = Integer.parseInt(txtWage.getText());
        int bonus = Integer.parseInt(txtBonus.getText());
        int signOnFee = Integer.parseInt(txtSignOnFee.getText());

        Date startDate = startDateChooser.getDate();
        Date endDate = endDateChooser.getDate();
        Enterprise seller = (Enterprise) cmbEnterprise.getSelectedItem();

        ContractWorkRequest contractRequest = new ContractWorkRequest();

        contractRequest.setPlayer(player);
        contractRequest.setBuyer(enterprise);
        contractRequest.setSeller(seller);
        contractRequest.setBonus(bonus);
        contractRequest.setSignOnFee(signOnFee);
        contractRequest.setWage(wage);
        contractRequest.setSender(userAccount);
        contractRequest.setStatus("Sent");
        contractRequest.setContractStartDate(startDate);
        contractRequest.setContractEndDate(endDate);
        managerOrganization.getWorkQueue().getWorkRequestList().remove(request);

//        for (Organization o : seller.getOrganizationDirectory().getOrganizationList()) {
//            if (o instanceof ManagerOrganization) {
//                o.getWorkQueue().getWorkRequestList().add(request);
//            }
//        }
        Enterprise enp = null;
        Organization org = null;
        for (Network network : system.getNetworkList()) {
            for (Enterprise enterprise : network.getEnterpriseDirectory().getEnterpriseList()) {
                if (enterprise.equals(seller)) {
                    enp = enterprise;
                    for (Organization organization : enterprise.getOrganizationDirectory().getOrganizationList()) {
                        if (organization instanceof ManagerOrganization) {
                            org = organization;
                            break;
                        }
                    }
                }
            }
        }
///???
        if (org != null) {
            org.getWorkQueue().getWorkRequestList().add(contractRequest);
        }
        JOptionPane.showMessageDialog(null, "Request Submitted!");

        populatePlayerTable();
    }//GEN-LAST:event_btnSendContractActionPerformed

