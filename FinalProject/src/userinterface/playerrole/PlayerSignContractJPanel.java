    public void populateRequestTable() {
        DefaultTableModel model = (DefaultTableModel) contractRequestJTable.getModel();

        model.setRowCount(0);
        for (WorkRequest request : account.getWorkQueue().getWorkRequestList()) {
            Object[] row = new Object[4];
            row[0] = request;
            ContractWorkRequest contractRequest = (ContractWorkRequest) request;
            row[1] = contractRequest.getSeller();
            row[2] = contractRequest.getSignOnFee();
            row[3] = contractRequest.getStatus();
            model.addRow(row);
        }
    }
