    private void setBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_setBtnActionPerformed
        // TODO add your handling code here:
        BossToManagerWorkRequest bossToManagerWorkRequest = new BossToManagerWorkRequest();        
        try{
            int ceiling = Integer.parseInt(ceilingPriceTextField.getText());
             int floor = Integer.parseInt(floorPriceTextField.getText());
             int target = Integer.parseInt(targetPriceTextField.getText());
                        
        
            bossToManagerWorkRequest.setPlayer(player);
            bossToManagerWorkRequest.setCeilingPrice(ceiling);
            bossToManagerWorkRequest.setFloorPrice(floor);
            bossToManagerWorkRequest.setTargetPrice(target);
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Please Insert Valid Price");
        }
        
        bossToManagerWorkRequest.setSender(account);
        bossToManagerWorkRequest.setStatus("Sent");
        
        Organization org = null;
        for (Organization organization : enterprise.getOrganizationDirectory().getOrganizationList()){
            if (organization instanceof ManagerOrganization){
                org = organization;
                break;
            }
        }
        if (org!=null){
            org.getWorkQueue().getWorkRequestList().add(bossToManagerWorkRequest);
            account.getWorkQueue().getWorkRequestList().add(bossToManagerWorkRequest);
            
            JOptionPane.showMessageDialog(null, "Request Submit Successful!!");

        }
        
    }//GEN-LAST:event_setBtnActionPerformed

