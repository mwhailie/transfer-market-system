    private void btnSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSubmitActionPerformed
        // TODO add your handling code here:
        
        

        String str = txtNum.getText();
        if (validRequestNumber(str)) {
            
        String position = String.valueOf(positionComboBox.getSelectedItem());
        
        CoachWorkRequest request = new CoachWorkRequest();

        request.setSender(userAccount);
        request.setStatus("Sent");
        request.setNumber(Integer.parseInt(str));
        request.setPosition(position);
        request.setSenderOrganzation(organization);
        
        
        Organization org = null;
        for (Organization organization : enterprise.getOrganizationDirectory().getOrganizationList()){
            if (organization instanceof ManagerOrganization){
                org = organization;
                break;
            }
        }
        if (org!=null){
            org.getWorkQueue().getWorkRequestList().add(request);
            userAccount.getWorkQueue().getWorkRequestList().add(request);
        }
        JOptionPane.showMessageDialog(null, "Request Submitted!");

        }
        
    }//GEN-LAST:event_btnSubmitActionPerformed

