    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        int row = playerJTable.getSelectedRow();
        if (row < 0) {
            JOptionPane.showMessageDialog(null, "Please select a row!!", "Warning", JOptionPane.WARNING_MESSAGE);
        }

        int earnMoney = 0;

        BossToManagerWorkRequest request = (BossToManagerWorkRequest) playerJTable.getValueAt(row, 0);
        UserAccount player = request.getPlayer();
        int wage = 0;
        int bonus = 0;
        int signOnFee = 0;
                
        try{
            wage = Integer.parseInt(txtWage.getText());
            bonus = Integer.parseInt(txtBonus.getText());
            signOnFee = Integer.parseInt(txtSignOnFee.getText());
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Please Insert Valid!","Warning!",  JOptionPane.WARNING_MESSAGE);
        }

        Date startDate = startDateChooser.getDate();
        Date endDate = endDateChooser.getDate();

        Long duration = startDate.getTime() - endDate.getTime();
        Long durationInWeek = duration / (1000 * 60 * 60 * 24 * 7);
        int durationInWeekInt = durationInWeek.intValue();
        Role role = player.getRole();

        if (role instanceof PlayerRole) {
            PlayerRole playerRole = (PlayerRole) role;
            playerRole.getCommercialValue();

            earnMoney = playerRole.getCommercialValue() - wage * durationInWeekInt - signOnFee - bonus;
            
        }
        if(earnMoney>0){
                JOptionPane.showMessageDialog(null, "This contract can make" + earnMoney +"$", "You Should Agree!", JOptionPane.WARNING_MESSAGE);
        } else if(earnMoney == 0){
                JOptionPane.showMessageDialog(null, "This contract can make" + earnMoney +"$", "You Should Consider Carefully!", JOptionPane.WARNING_MESSAGE);
        }else if(earnMoney < 0){
                JOptionPane.showMessageDialog(null, "This contract can lose" + earnMoney +"$", "You Should Not Agree!!", JOptionPane.WARNING_MESSAGE);
        }

    }//GEN-LAST:event_jButton1ActionPerformed

