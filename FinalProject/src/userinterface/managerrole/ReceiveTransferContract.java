    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:

        int row = playerJTable.getSelectedRow();
        if (row < 0) {
            JOptionPane.showMessageDialog(null, "Please select a row!!", "Warning", JOptionPane.WARNING_MESSAGE);
        }

        ContractWorkRequest request = (ContractWorkRequest) playerJTable.getValueAt(row, 0);

        UserAccount player = request.getPlayer();

        int signOnFee = request.getSignOnFee();//sign on fee is money that a club can get from the transfer contracts

        Role role = player.getRole();

        if (role instanceof PlayerRole) {
            PlayerRole playerRole = (PlayerRole) role;
            
            Date contractEndDate = playerRole.getLastestContract().getContractEndDate();
            int wage = playerRole.getLastestContract().getWage();
            int earnMoney = 0;
            
            if (contractEndDate.before(new Date())) {//if contract is not end yet
                
                earnMoney = signOnFee - playerRole.getCommercialValue();
                
            }else{//if contract is end                
                Long remainDuration = ((new Date()).getTime()-contractEndDate.getTime());
                Long remainDurationInWeek = remainDuration/(1000*60*60*24*7);
                int remainWeekInt = remainDurationInWeek.intValue();
                earnMoney = signOnFee - playerRole.getCommercialValue() + wage*remainWeekInt;                
            }
            
            if(earnMoney>0){
                JOptionPane.showMessageDialog(null, "This contract can make" + earnMoney +"$", "You Should Agree!", JOptionPane.WARNING_MESSAGE);
            } else if(earnMoney == 0){
                JOptionPane.showMessageDialog(null, "This contract can make" + earnMoney +"$", "You Should Consider Carefully!", JOptionPane.WARNING_MESSAGE);
            }else if(earnMoney < 0){
                JOptionPane.showMessageDialog(null, "This contract can lose" + earnMoney +"$", "You Should Not Agree!!", JOptionPane.WARNING_MESSAGE);
            }
        }                

    }//GEN-LAST:event_jButton1ActionPerformed


